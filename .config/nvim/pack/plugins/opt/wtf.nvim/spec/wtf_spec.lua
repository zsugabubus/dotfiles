describe('visual mode', function()
	before_each(function()
		vim:set_lines({ '|X<--O-->X|' })
		vim:feed('5l')
	end)

	test('f', function()
		vim:feed('vfXd')
		vim:assert_lines({ '|X<--|' })
	end)

	test('F', function()
