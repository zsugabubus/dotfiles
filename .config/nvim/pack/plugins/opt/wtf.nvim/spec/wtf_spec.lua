		vim:assert_lines({ '|-->X|' })
	end)

	test('t', function()
		vim:feed('vtXd')
		vim:assert_lines({ '|X<--X|' })
	end)

	test('T', function()
		vim:feed('vTXd')
		vim:assert_lines({ '|X-->X|' })
	end)
end)

describe('operator-pending mode', function()
	before_each(function()
		vim:set_lines({ 'X', 'X<--O-->X', 'X' })
		vim:feed('j4l')
	end)

	test('f', function()
		vim:feed('dfX')
		vim:assert_lines({ 'X', 'X<--', 'X' })
		vim:feed('.')
		vim:assert_lines({ 'X', 'X<-' })
	end)

	test('F', function()
		vim:feed('dFX')
		vim:assert_lines({ 'X', 'O-->X', 'X' })
		vim:feed('.')
		vim:assert_lines({ 'O-->X', 'X' })
	end)

	test('t', function()
		vim.o.virtualedit = 'all'
		vim:feed('dtX')
		vim:assert_lines({ 'X', 'X<--X', 'X' })
		vim:feed('.')
		pending('Likely unfixable')
		vim:assert_lines({ 'X', 'X<--X' })
	end)

	test('T', function()
		vim:feed('dTX')
		vim:assert_lines({ 'X', 'XO-->X', 'X' })
		vim:feed('.')
		pending('Likely unfixable')
		vim:assert_lines({ 'XO-->X', 'X' })
	end)
end)

describe('f with character', function()
	local function case(c)
		describe(c, function()
			test('stops anywhere', function()
				vim:set_lines({ 'xxx' .. c .. 'xxx' })
				vim:feed('f' .. c .. 'rf')
				vim:assert_lines({ 'xxxfxxx' })
			end)
		end)
	end

	case("'")
